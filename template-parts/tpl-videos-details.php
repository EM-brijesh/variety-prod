                    <p class="auther-name">By Emily Longeretta Plus Icon</p>
                    <p>It’s been quite a year for Kathy Bates and Billy Bob Thornton, who both lead hit shows. Bates stars in CBS’ “Matlock,” a reimagining of the ’80s drama, playing a scheming attorney who hides her true identity from her colleagues to obtain justice for her daughter. Meanwhile, Thornton transforms into the chain-smoking, ruthless oil company executive Tommy Norris on Taylor Sheridan’s “Landman” on Paramount+.</p>

                    <p>While on completely different paths as actors, they’ve known each other for three decades, first teaming up on Mike Nichols’ “Primary Colors,” where Bates tried to learn from the legendary director, and turned to him for advice. “I said, ‘What can you tell me about working with an actor?’” Bates recalls asking Nichols. “And he said, ‘Just love them.’”</p>

                    <p>But as it turns out, the pair almost met before “Primary Colors” — on the set of Bates’ most famous, and Oscar-winning, movie.</p>

                    <p><b>BILLY BOB THORNTON:</b> There was a time I went in and read for Rob Reiner for “Misery.”</p>

                    <p>KATHY BATES: You did not.</p>

                    <p>THORNTON: I swear to God. Originally, Richard Farnsworth, who played the sheriff, had a deputy. I saw Rob Reiner for it. And Rob said right in the room, “You’re the guy. We can send everybody else home.”</p>

                    <p>I was very excited. And I got a call from Rob Reiner — not many directors would do this — he called me and he said, “Listen, I’ve been looking at the script and been planning out what I’m going to do with this movie.” He said, “You can come up here and shoot this for the money or the insurance or whatever you need, but I’m just telling you, it’s not going to be in the movie.”</p>

                    <p><b>BATES:</b> Oh, wow.</p>

                    <p><b>THORNTON:</b> He said, “I don’t want you to come up here expecting that this is going to be in the movie. And I didn’t want you to be disappointed.” He said, “But I’ll leave it up to you. Do you want to come shoot it anyway?” And I said, “No.”</p>

                    <p>I thought that was very cool of him.</p>

                    <p><b>BATES:</b> Yeah, very cool.</p>

                    <p>Can we talk about “Landman”? I have so many questions.</p>

                    <p><b>THORNTON:</b> I’m happy to answer.</p>

                    <p><b>BATES:</b> We had this cocktail thing; Sharon Stone and I hosted. I was asking you about how you played this part, and she interrupted. I was so fucking pissed off! Did you have any time to really get together [with the cast] and get to know each other, or did you just jump in?</p>

                    <p><b>THORNTON:</b> We had to kind of jump in. We had one cast dinner before we started. I don’t know if this makes any sense or not, but they’re all such specific personalities that the chemistry almost happened. Ali Larter [who plays Thornton’s feisty ex-wife] bursts through the room wearing half a sarong and starts bossing me around. It just makes you go, “What are you talking about, honey? Leave me alone. I’m trying to watch TV.” And Michelle Randolph’s like a kitten.</p>

                    <p><b>BATES:</b> Oh my God.</p>

                    <p><b>THORNTON:</b> I’ve got a 20-year-old daughter in college, so that’s why it’s so easy for me to play that. At the same time, the stuff she says to me? Oh my God. We really did become a family, and I know people say that a lot, but it really is true in this case.</p>

                    <p>The first television role I ever did was “Matlock.”</p>

                    <p><b>BATES:</b> I didn’t watch it. Everybody always asked me that — did I watch the show?</p>

                    <p><b>THORNTON:</b> And that was a conscious choice.</p>

                    <p><b>BATES:</b> Well, let’s say it was a conscious choice. I watched a couple to see what I could get out of it, but our show is just so different. I feel like this part was written with me in mind. Jennie Snyder Urman created it. And I’m lucky because I heard, originally, they wanted to make [my character] Andy Griffith’s great-great-granddaughter. So she’d be 30-something. But Jennie took a walk, and it came to her that she wanted to write something about older women and feeling invisible.</p>

                    <p>When I first read the script, I thought, this is just episodic, and I’m not interested in doing that. And then I finally got to the end, and there’s this twist. I said, “Oh, yeah, I’m in. I’m in.” Because I really had one foot out the door.</p>

                    <p><b>THORNTON:</b> Really?</p>

                    <p><b>BATES:</b> I just felt that I was getting small roles in films that I loved that people were not seeing. I just began to ask myself, “Is this what I want to keep doing? Do I want to sell the house and maybe move to France and call it a day?” And then I got this script. It just fit me like a glove.</p>
