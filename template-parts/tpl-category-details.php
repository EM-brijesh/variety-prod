              <p>Legendary Australian rockers AC/DC are touring Australia this year.</p>
              <p> AC/DC will make their long-overdue return home for a run of stadium shows, kicking off November 12 at Melbourne Cricket Ground.</p>
              <p>Announced Monday, June 23, the national lap will include dates in Sydney, Adelaide, Perth, before wrapping up in Brisbane on December 14 (see full dates below). AC/DC will be supported by Amyl and the Sniffers at all shows.</p>
              <p>Also confirmed is the touring lineup, which features Angus Young (lead guitar), Brian Johnson (vocals), Stevie Young (rhythm guitar), Matt Laug (drums) and Chris Chaney (bass).</p>
              <p>Rolling Stone AU/NZ tipped the visit last week.</p>
              <p> Produced by TEG, AC/DC’s forthcoming trek will be their first here since <a href="https://www.billboard.com/music/music-news/acdc-announces-stadium-tour-of-australia-new-zealand-6531157/" target="_blank">2015</a>, the domestic leg of their “Rock or Bust” <a href="https://www.ac-dc.net/acdc_tour/acdc_tickets_tour_dates_2016.php" target="_blank">world tour.</a></p>
              <p>On that run, Angus Young and co. shifted more than 520,000 tickets across 11 coast-to-coast dates, including shows in Auckland and Wellington.</p>
              <p>Prior to that, AC/DC smashed records with their “Black Ice” jaunt in 2009, which shifted about 750,000 tickets, including 650,000 in Australia alone.</p>
              <p>Those tours were produced by Melbourne-based Van Egmond Group, now part of TEG Group, through an acquisition completed in 2020. Van Egmond Group is led by managing director Christo Van Egmond, whose father, the late impresario Garry Van Egmond, AC/DC’s long-time go-to man for staging live shows in Australasia.</p>
              <p>AC/DC should be in fine form by the time they arrive in these parts. The Rock Hall-inducted band set out on another pan-European stadium run on June 26 at Letňany Airport in Prague, Czech Republic, their second in 12 months. The itinerary includes stops in Germany, Italy, Poland, Spain, Estonia, Sweden, Norway, France, and Scotland. The North American leg of AC/DC’s “Power Up” tour concluded on May 28, 2025, with a final show in Cleveland, OH.</p>
              <p>Expect to hear classics from across the band’s 17 studio albums, including the latest, 2020’s Power Up, which gave them a sixth No. 1 on the ARIA Albums Chart, and leaders on the Billboard 200 and Official UK Albums Chart. The touring lineup features Angus Young on lead guitar, vocalist Brian Johnson, rhythm guitarist Stevie Young, drummer Matt Laug and bass player Chris Chaney.</p>
              <p>AC/DC are one of only two bands inducted into both the Rock Hall and the ARIA Hall of Fame (the Bee Gees are the other band). They got the Rock Hall nod in 2003, and theirs was one of the first names read out for induction into the ARIA Hall of Fame, back in 1988.</p>
